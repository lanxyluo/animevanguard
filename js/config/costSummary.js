// Utility functions for cost summary
export const costSummaryUtils = {
    // Get cost summary for a specific unit
    getCostSummaryForUnit(unitId) {
        return COST_SUMMARY_DATA[unitId] || null;
    },

    // Calculate total cost for a unit
    calculateTotalCost(unitId) {
        const costData = COST_SUMMARY_DATA[unitId];
        if (!costData) return 0;

        let totalCost = costData.totalGold || 0;
        
        // Add material costs (if they have numeric cost)
        if (costData.materialCosts) {
            costData.materialCosts.forEach(material => {
                const costStr = material.estimatedCost;
                if (costStr.includes('Gold')) {
                    const cost = parseInt(costStr.replace(/[^\d]/g, ''));
                    if (!isNaN(cost)) {
                        totalCost += cost;
                    }
                }
            });
        }

        return totalCost;
    },

    // Get difficulty level for a unit
    getDifficultyLevel(unitId) {
        const costData = COST_SUMMARY_DATA[unitId];
        if (!costData) return 'Unknown';

        const difficulty = costData.difficulty?.toLowerCase();
        
        if (difficulty?.includes('extreme')) return 'extreme';
        if (difficulty?.includes('very hard')) return 'very-hard';
        if (difficulty?.includes('hard')) return 'hard';
        if (difficulty?.includes('medium')) return 'medium';
        if (difficulty?.includes('easy')) return 'easy';
        
        return 'medium';
    },

    // Get time investment estimate
    getTimeInvestment(unitId) {
        const costData = COST_SUMMARY_DATA[unitId];
        return costData ? costData.timeInvestment : 'Unknown';
    },

    // Check if unit is worth the investment
    isWorthInvestment(unitId) {
        const costData = COST_SUMMARY_DATA[unitId];
        return costData ? costData.worthIt : false;
    },

    // Get investment notes
    getInvestmentNotes(unitId) {
        const costData = COST_SUMMARY_DATA[unitId];
        return costData ? costData.notes : '';
    },

    // Validate cost summary data
    validateCostSummaryData() {
        console.log('🔍 Validating cost summary data...');
        
        for (const [unitId, data] of Object.entries(COST_SUMMARY_DATA)) {
            if (typeof data.totalGold !== 'number') {
                console.error(`❌ Invalid totalGold for ${unitId}`);
                return false;
            }

            if (!data.materialCosts || !Array.isArray(data.materialCosts)) {
                console.error(`❌ Invalid materialCosts for ${unitId}`);
                return false;
            }

            if (!data.totalEstimatedCost || typeof data.totalEstimatedCost !== 'string') {
                console.error(`❌ Invalid totalEstimatedCost for ${unitId}`);
                return false;
            }

            if (!data.timeInvestment || typeof data.timeInvestment !== 'string') {
                console.error(`❌ Invalid timeInvestment for ${unitId}`);
                return false;
            }

            if (typeof data.difficulty !== 'string') {
                console.error(`❌ Invalid difficulty for ${unitId}`);
                return false;
            }

            if (typeof data.worthIt !== 'boolean') {
                console.error(`❌ Invalid worthIt for ${unitId}`);
                return false;
            }

            if (!data.notes || typeof data.notes !== 'string') {
                console.error(`❌ Invalid notes for ${unitId}`);
                return false;
            }
        }

        console.log('✅ Cost summary data validation passed');
        return true;
    }
}; 